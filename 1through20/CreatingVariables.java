public class CreatingVariables {
  public static void main( String[] args ) {
    int x, y, age, ravens;
    double seconds, e, checking, runes;
    String firstName, lastName, title, secondName, thirdName;

    x = 10;
    y = 400;
    age = 39;
    ravens = 2;

    seconds = 4.71;
    e = 2.71828182845904523536;
    checking = 1.89;
    runes = 27;

    firstName = "Graham";
    lastName = "Mitchell";
    title = "Mr.";
    secondName = "As-High";
    thirdName = "Third";

    System.out.println( "The variable x contains " + x );
    System.out.println( "The value " + y + " is stored in the variable y.");
    System.out.println( "The experiment took " + seconds + " seconds." );
    System.out.println( "A favorite irrational # is Euler's number " + e );
    System.out.println( "Hopefully you have more than $" + checking + "!" );
    System.out.println( "My name is " + title + " " + firstName + " " + lastName );
    System.out.println( "I am called Highest, " + secondName + ", and " + thirdName + "." );
    System.out.println( "I have " + ravens + " ravens and have learned " + runes + " secrets." );
  }
}
